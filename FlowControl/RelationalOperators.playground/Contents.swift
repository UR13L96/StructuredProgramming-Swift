print("Operadores relacionales")

let valueA: Float = 5
let valueB: Float = 10

if valueA < valueB {
    print("Condición 1: Cierto")
} else {
    print("Condición 1: Falso")
}

if valueA <= valueB {
    print("Condición 2: Cierto")
} else {
    print("Condición 2: Falso")
}

if valueA > valueB {
    print("Condición 3: Cierto")
} else {
    print("Condición 3: Falso")
}

if valueA >= valueB {
    print("Condición 4: Cierto")
} else {
    print("Condición 4: Falso")
}

if valueA == valueB {
    print("Condición 5: Cierto")
} else {
    print("Condición 5: Falso")
}

if valueA != valueB {
    print("Condición 6: Cierto")
} else {
    print("Condición 6: Falso")
}
